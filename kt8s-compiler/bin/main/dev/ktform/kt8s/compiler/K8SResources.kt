package dev.ktform.kt8s.compiler

import com.google.devtools.ksp.processing.*
import com.google.devtools.ksp.symbol.*
import com.squareup.kotlinpoet.ClassName
import com.squareup.kotlinpoet.CodeBlock
import com.squareup.kotlinpoet.FileSpec
import com.squareup.kotlinpoet.FunSpec
import com.squareup.kotlinpoet.KModifier
import com.squareup.kotlinpoet.LIST
import com.squareup.kotlinpoet.MAP
import com.squareup.kotlinpoet.ParameterizedTypeName.Companion.parameterizedBy
import com.squareup.kotlinpoet.PropertySpec
import com.squareup.kotlinpoet.STRING
import com.squareup.kotlinpoet.TypeSpec
import java.io.OutputStreamWriter

class K8SResourceClientGenerator(
    private val environment: SymbolProcessorEnvironment
) : SymbolProcessor {
    private val logger = environment.logger
    private val codeGenerator = environment.codeGenerator

  override fun process(resolver: Resolver): List<KSAnnotated> {
    when (val result = JSONSchema.load(JSONSchema.Version.V1_33_0)) {
      is arrow.core.Either.Left -> {
        logger.error("K8SResourceClientGenerator: Failed to load schema: ${result.value}")
      }
      is arrow.core.Either.Right -> {
        try {
          generateResourcesRegistry(result.value)
        } catch (t: Throwable) {
          logger.error("K8SResourceClientGenerator: Code generation error: ${t.message}")
        }
      }
    }

    return emptyList()
  }

  private fun generateResourcesRegistry(definitions: List<JSONSchema.Definition>) {
    val fileSpec = buildResourcesRegistryFile(definitions)

    val file = codeGenerator.createNewFile(
      dependencies = Dependencies(false),
      packageName = fileSpec.packageName,
      fileName = fileSpec.name,
      extensionName = "kt"
    )

    OutputStreamWriter(file, Charsets.UTF_8).use { out ->
      out.write(fileSpec.toString())
    }
  }

  companion object {

    internal fun buildResourcesRegistryContent(
      definitions: List<JSONSchema.Definition>
    ): Triple<String, String, String> {
      val file = buildResourcesRegistryFile(definitions)
      return Triple(file.packageName, file.name, file.toString())
    }

    internal fun buildResourcesRegistryFile(
      definitions: List<JSONSchema.Definition>
    ): FileSpec {
      // Build a map: kind -> list of (group, version, kind)
      val kindToGVKs: Map<String, List<Triple<String, String, String>>> =
        definitions
          .filter { it.kubernetesGroupVersionKind.isNotEmpty() }
          .groupBy(
            keySelector = { it.kubernetesGroupVersionKind.first().third },
            valueTransform = { it.kubernetesGroupVersionKind }
          )
          .mapValues { (_, lists) -> lists.flatten().distinct() }

      val pkg = "dev.ktform.kt8s.resources"
      val fileName = "K8sResources"

      val gvkClass = TypeSpec.classBuilder("GVK")
        .addModifiers(KModifier.DATA)
        .primaryConstructor(
          FunSpec.constructorBuilder()
            .addParameter("group", STRING)
            .addParameter("version", STRING)
            .addParameter("kind", STRING)
            .build()
        )
        .addProperty(PropertySpec.builder("group", STRING).initializer("group").build())
        .addProperty(PropertySpec.builder("version", STRING).initializer("version").build())
        .addProperty(PropertySpec.builder("kind", STRING).initializer("kind").build())
        .build()

      val gvkClassName = ClassName(pkg, fileName).nestedClass("GVK")
      val kindsType = MAP.parameterizedBy(STRING, LIST.parameterizedBy(gvkClassName))

      val kindsInit = CodeBlock.builder().apply {
        add("mapOf(\n")
        val entries = kindToGVKs.entries.sortedBy { it.key }
        entries.forEachIndexed { idx, (kind, gvkList) ->
          add("  %S to listOf(", kind)
          gvkList.forEachIndexed { i, (g, v, k) ->
            if (i > 0) add(", ")
            add("GVK(%S, %S, %S)", g, v, k)
          }
          add(")")
          if (idx < entries.size - 1) add(",")
          add("\n")
        }
        add(")")
      }.build()

      val kindsProp = PropertySpec.builder("kinds", kindsType)
        .initializer(kindsInit)
        .build()

      val gvkOfFun = FunSpec.builder("gvkOf")
        .addParameter("kind", STRING)
        .returns(LIST.parameterizedBy(gvkClassName))
        .addStatement("return kinds[kind].orEmpty()")
        .build()

      val obj = TypeSpec.objectBuilder(fileName)
        .addType(gvkClass)
        .addProperty(kindsProp)
        .addFunction(gvkOfFun)
        .addKdoc(
          "Generated by K8SResourceClientGenerator from Kubernetes OpenAPI schema.\n" +
            "Exposes available Kinds and their Group/Version/Kind combinations.\n"
        )
        .build()

      return FileSpec.builder(pkg, fileName)
        .addType(obj)
        .build()
    }
  }
}
