package dev.ktform.kt8s.resources

import kotlin.String
import kotlinx.serialization.SerialName
import kotlinx.serialization.Serializable
import kotlinx.serialization.Transient

/**
 * @param resourceName Name of the resource to which this resource resize policy applies. Supported
 *   values: cpu, memory.
 * @param restartPolicy Restart policy to apply when specified resource is resized. If not
 *   specified, it defaults to NotRequired.
 */
@Serializable
public data class ContainerResizePolicy(
    public val resourceName: String,
    public val restartPolicy: String,
) : Resource {
    @SerialName("apiVersion") override val apiVersion: String = "io.k8s.api.core/v1"

    @Transient override val group: String = "io.k8s.api.core"

    @Transient override val version: String = "v1"

    @SerialName("kind") override val kind: String = "ContainerResizePolicy"
}
