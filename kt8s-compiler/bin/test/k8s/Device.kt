/**
 * @param basic Basic defines one device instance.
 * @param name Name is unique identifier among all devices managed by the driver in the pool. It must be a DNS label.
 */
@Serializable
public data class Device(
  public val basic: BasicDevice,
  public val name: String,
) : Resource {
  @SerialName("apiVersion")
  override val apiVersion: String = "io.k8s.api.resource/v1alpha3"

  @Transient
  override val group: String = "io.k8s.api.resource"

  @Transient
  override val version: String = "v1alpha3"

  @SerialName("kind")
  override val kind: String = "Device"
}