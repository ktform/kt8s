package dev.ktform.kt8s.resources

import kotlin.String
import kotlinx.serialization.SerialName
import kotlinx.serialization.Serializable
import kotlinx.serialization.Transient

/**
 * @param datasetName datasetName is Name of the dataset stored as metadata -> name on the dataset
 *   for Flocker should be considered as deprecated
 * @param datasetUUID datasetUUID is the UUID of the dataset. This is unique identifier of a Flocker
 *   dataset
 */
@Serializable
public data class FlockerVolumeSource(
    public val datasetName: String,
    public val datasetUUID: String,
) : Resource {
    @SerialName("apiVersion") override val apiVersion: String = "io.k8s.api.core/v1"

    @Transient override val group: String = "io.k8s.api.core"

    @Transient override val version: String = "v1"

    @SerialName("kind") override val kind: String = "FlockerVolumeSource"
}
