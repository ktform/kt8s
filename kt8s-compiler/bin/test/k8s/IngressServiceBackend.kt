package dev.ktform.kt8s.resources

import kotlin.String
import kotlinx.serialization.SerialName
import kotlinx.serialization.Serializable
import kotlinx.serialization.Transient

/**
 * @param name name is the referenced service. The service must exist in the same namespace as the
 *   Ingress object.
 * @param port port of the referenced service. A port name or port number is required for a
 *   IngressServiceBackend.
 */
@Serializable
public data class IngressServiceBackend(
    public val name: String,
    public val port: ServiceBackendPort,
) : Resource {
    @SerialName("apiVersion") override val apiVersion: String = "io.k8s.api.networking/v1"

    @Transient override val group: String = "io.k8s.api.networking"

    @Transient override val version: String = "v1"

    @SerialName("kind") override val kind: String = "IngressServiceBackend"
}
